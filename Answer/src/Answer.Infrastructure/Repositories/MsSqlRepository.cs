using Answer.Application.Interfaces;
using Answer.Domain.Common;
using Answer.Infrastructure.Data;
using Microsoft.EntityFrameworkCore;

namespace Answer.Infrastructure.Repositories;

public class MsSqlRepository<T> : IRepository<T> where T : BaseEntity
{
    private readonly AnswerDbContext _context;
    private readonly DbSet<T> _dbSet;

    public MsSqlRepository(AnswerDbContext context)
    {
        _context = context;
        _dbSet = context.Set<T>();
    }

    public async Task<T?> GetByIdAsync(Guid id)
    {
        return await _dbSet.FindAsync(id);
    }

    public async Task<IEnumerable<T>> GetAllAsync()
    {
        return await _dbSet.ToListAsync();
    }

    public async Task<T> AddAsync(T entity)
    {
        await _dbSet.AddAsync(entity);
        await _context.SaveChangesAsync();
        return entity;
    }

    public async Task UpdateAsync(T entity)
    {
        _dbSet.Update(entity);
        await _context.SaveChangesAsync();
    }

    public async Task DeleteAsync(Guid id)
    {
        var entity = await GetByIdAsync(id);
        if (entity != null)
        {
            _dbSet.Remove(entity);
            await _context.SaveChangesAsync();
        }
    }
}
